                    <h2 class="section-heading">Reporting</h2>

                    <p>
                        Any time that you feel this code of conduct is being broken, or the conduct of speakers or attendees is distressing to you, you should always feel
                        comfortable to report it, knowing that you will be taken seriously, and that it will be appropriately investigated and dealt with. There are a few different
                        avenues for reporting.
                    </p>

                    <ul>
                        <li>Personally, to members of the organising committee by emailing <a href="mailto: codeofconduct@melbournecocoaheads.com">codeofconduct@melbournecocoaheads.com</a></li>
                        <li>Anonymously, at <a href="https://melbournecocoaheads.typeform.com/to/fLjfOi">https://melbournecocoaheads.typeform.com/to/fLjfOi</a>.
                            Anonymous reports will go to all members of the organising committee and Values and Diversity committee.</li>
                    </ul>

                    <p>
                        All complaints made in any of these ways will remain confidential, be taken seriously, investigated, and dealt with appropriately.
                    </p>

                    <p>
                        Personal reports are always much better since there is only so much investigation that can be done without the ability to
                        speak to the reporting party. When you report personally to any of the above people, please make sure you tell them if there
                        is anyone specifically you do not want them to speak to about the issue. Normally the person you report to will discuss the report
                        with other team members and/or the reporting mentors.
                    </p>

                    <hr>

                    <h2 class="section-heading">What happens after you file a report?</h2>

                    <p>You will receive an email from the organising committee acknowledging receipt of your report.
                        The committee will meet to review the incident and determine:
                    </p>

                    <ul>
                        <li>What happened.</li>
                        <li>Whether this event constitutes a code of conduct violation.</li>
                        <li>Who the bad actor was.</li>
                        <li>Whether this is an ongoing situation, or if there is a threat to anyone's physical safety.</li>
                    </ul>

                    <p>If this is determined to be an ongoing incident or a threat to physical safety, the committee's immediate priority will be to protect
                        everyone involved. This means we may delay an "official" response until we believe that the situation has ended and that everyone is physically safe.
                        Once the committee has a complete account of the events they will make a decision as to how to response. Responses may include:
                    </p>

                    <ul>
                        <li>Nothing (if we determine no violation occurred).</li>
                        <li>A private reprimand from the committee to the individual(s) involved.</li>
                        <li>A public reprimand.</li>
                        <li>An imposed vacation (i.e. asking someone to "take a week off" from events or Slack).</li>
                        <li>A permanent or temporary ban from some or all Melbourne Cocoaheads spaces (events, Slack, etc.)</li>
                        <li>A request for a public or private apology.</li>
                    </ul>

                    <p>
                        Unless theyâ€™re anonymous, we'll respond to the person who filed the report with either a resolution or an explanation of why the situation is not yet resolved.
                    </p>

                    <p>
                        Once we've determined our final action, we'll contact the original reporter (if we can) to let them know what action (if any) we'll be taking.
                        We'll take into account feedback from the reporter on the appropriateness of our response, but we don't guarantee we'll act on it.
                    </p>

