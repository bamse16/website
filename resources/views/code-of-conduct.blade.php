                    <h2 class="section-heading">Speakers</h2>

                    <p>
                        Presentation material should be appropriate for people of all ages.<br>
                        Any public presentation which is part of any event, including but not limited to presentations, lightning talks, recruiter/job position or other promotions,
                        mailing list posts and forums, is subject to this code of conduct and thus may not contain:
                    </p>

                    <ul>
                        <li>sexual or violent imagery.</li>
                        <li>exclusionary language.</li>
                        <li>insults or personal attacks.</li>
                    </ul>

                    <p>Presenters are asked to avoid language which is not appropriate for an all-ages audience as much as possible.<br>
                        If the subject matter of the presentation cannot be presented adequately without including language that could be considered offensive, this should be
                        pointed out in advance, at the beginning of the talk and in the schedule.<br>
                        If presenters are unsure whether their material is suitable, they are encouraged to show it to an organiser before their session.
                    </p>

