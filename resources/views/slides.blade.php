# About Cocoaheads

- Established July 2007
- 2nd Thursday of the month
- Monthly Hack nights (final Tuesday of the month)
- Sponsored by the Melbourne community
- ~2-3 presentations @ 20-25 minutes
- n * lightning talks @ ~5-10 minutes
- Drinks and pizza

